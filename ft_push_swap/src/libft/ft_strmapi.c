#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;
	char			t;

	if (!s || !f)
		return (NULL);
	i = 0;
	t = (sizeof(char) * (ft_strlen(s)) + 1);
	str = (char *)malloc(t);
	if (str == NULL)
		return (NULL);
	while (*(s + i) != '\0')
	{
		*(str + i) = f(i, *(s + i));
		i++;
	}
	*(str + i) = '\0';
	return (str);
}
